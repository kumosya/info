/* Boot-time GE and PF stubs used by early boot IDT; these call C handlers
   and then halt (they don't return). */
gp_fault_stub:
    cli
    call boot_gp_fault_handler
1:  hlt
    jmp 1b

page_fault_stub:
    cli
    mov %cr2, %rdi
    call boot_page_fault_handler
2:  hlt
    jmp 2b
